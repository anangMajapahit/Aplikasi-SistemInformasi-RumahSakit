            String sql = "INSERT INTO pasien_covid19 VALUES ('"+id_pasien.getText()+"','"+nama_pasien.getText()+"','"+alamat.getText()+"','"+umur.getText()+"','"+kode_pos.getText()+"','"+nomor_telpon.getText()+"')";
            java.sql.Connection conn =  (Connection)config.configDB();
            java.sql.PreparedStatement pstm =conn.prepareStatement(sql);
