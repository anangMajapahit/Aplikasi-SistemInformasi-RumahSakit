        alamat.setText(Alamat);
        
        String Umur = table_pasiencovid19.getValueAt(baris,4).toString();
        umur.setText(Umur);
        
        String KodePos = table_pasiencovid19.getValueAt(baris,5).toString();
        kode_pos.setText(KodePos);
        
        String NomorTelpon = table_pasiencovid19.getValueAt(baris,6).toString();
        nomor_telpon.setText(NomorTelpon);                         
