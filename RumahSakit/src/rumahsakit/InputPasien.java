    private void tablepasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablepasienMouseClicked
        // TODO add your handling code here:
        int baris = tablepasien.rowAtPoint(evt.getPoint());
        String Id_Pasien = tablepasien.getValueAt(baris,1).toString();
        id_pasien.setText(Id_Pasien);
        
        String Nama_Pasien = tablepasien.getValueAt(baris,2).toString();
        nama_pasien.setText(Nama_Pasien);
        
        String Asal_Daerah = tablepasien.getValueAt(baris,3).toString();
        asal_daerah.setSelectedItem(Asal_Daerah);
        
        String Alamat = tablepasien.getValueAt(baris,4).toString();
        alamatlengkap.setText(Alamat);
        
        String Penyakit_diDerita = tablepasien.getValueAt(baris,5).toString();
        penyakit_diderita.setText(Penyakit_diDerita);
        
        String Kode_Pos = tablepasien.getValueAt(baris,6).toString();
        kode_pos.setText(Kode_Pos);  
        
        String Nomor_Telpon = tablepasien.getValueAt(baris,7).toString();
        nomer_telpon.setText(Nomor_Telpon);  
        
    }//GEN-LAST:event_tablepasienMouseClicked

