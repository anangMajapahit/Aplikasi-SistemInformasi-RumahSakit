    private void tabledokterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabledokterMouseClicked
        // TODO add your handling code here:
        int baris = tabledokter.rowAtPoint(evt.getPoint());
        String Id_Dokter = tabledokter.getValueAt(baris,1).toString();
        id_dokter.setText(Id_Dokter);
        
        String Nama_Dokter = tabledokter.getValueAt(baris,2).toString();
        nama_dokter.setText(Nama_Dokter);
        
        String Spesialis_Keahlian = tabledokter.getValueAt(baris,3).toString();
        spesialis_keahlian.setText(Spesialis_Keahlian);
        
        String Asal_Daerah = tabledokter.getValueAt(baris,4).toString();
        asal_daerah.setSelectedItem(Asal_Daerah);
        
        String Alamat = tabledokter.getValueAt(baris,5).toString();
        alamat.setText(Alamat);
        
        String Kode_Pos = tabledokter.getValueAt(baris,6).toString();
        kode_pos.setText(Kode_Pos);  
        
        String Nomer_Telpon = tabledokter.getValueAt(baris,7).toString();
        nomor_telpon.setText(Nomer_Telpon);  
    }//GEN-LAST:event_tabledokterMouseClicked

